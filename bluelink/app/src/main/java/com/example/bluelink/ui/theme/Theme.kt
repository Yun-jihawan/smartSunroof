@Composable
fun BluelinkTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColors
        else -> LightColors
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            // 상태 표시줄 및 내비게이션 바 색상 제어 (선택적)
            // 예시: 상태 표시줄을 투명하게 하고, 앱 컨텐츠가 뒤로 가도록 (Edge-to-Edge)
            // window.statusBarColor = Color.Transparent.toArgb() // API 23+
            // window.navigationBarColor = Color.Transparent.toArgb() // API 27+
            // WindowCompat.setDecorFitsSystemWindows(window, false) // Edge-to-Edge 활성화

            // 또는 특정 색상으로 설정
            window.statusBarColor = colorScheme.surfaceColorAtElevation(3.dp).toArgb() // 예시
            window.navigationBarColor = colorScheme.surfaceColorAtElevation(3.dp).toArgb() // 예시

            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography, // Type.kt에서 정의한 AppTypography 사용
        shapes = AppShapes,       // Shape.kt에서 정의한 AppShapes 사용
        content = content
    )
}